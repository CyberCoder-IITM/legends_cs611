/*
 * All possible moves
 */
public enum Move {
    LEFT, // move left
    RIGHT, // move right
    UP, // move up
    DOWN, // move down
    INFO, // show hero information
    MARKET, // go into market
    PRINT, // print world
    INVENTORY, // change/apply inventory
    QUIT, // quit game
    HELP, // help
    NONE;
}
